CREATE TABLE HOTEL(
    ID NUMBER,
    CONSTRAINT HOTEL_PK PRIMARY KEY (ID),
    NAME NVARCHAR2(50),
    COUNTRY NVARCHAR2(50),
    CITY NVARCHAR2(50),
    STREET NVARCHAR2(50)
);

CREATE TABLE ROOMS (
    ID NUMBER,
    CONSTRAINT ROOM_PK PRIMARY KEY (ID),
    PRICE NUMBER (*,2),
    BREAKFAST_INCLUDED NUMBER (1) DEFAULT 1,
    CHECK (BREAKFAST_INCLUDED BETWEEN  0 AND 1),
    PETS_ALLOWED NUMBER (1) DEFAULT (0),
    CHECK (PETS_ALLOWED BETWEEN  0 AND 1),
    DATE_AVAILABLE_FROM DATE,
    HOTEL NUMBER,
    CONSTRAINT HOTEl_FK FOREIGN KEY (HOTEL) REFERENCES HOTEL(ID)
);

CREATE TABLE USERS(
    ID NUMBER,
    CONSTRAINT ROOM_PK PRIMARY KEY (ID),
    USER_NAME NVARCHAR2(50),
    PASSWORD NVARCHAR2(50),
    COUNTRY NVARCHAR2(50),
    USER_TYPE NVARCHAR2(5),
    CHECK (USER_TYPE ='ADMIN' OR USER_TYPE = 'USER'),
);

CREATE TABLE ORDERS(
    ID NUMBER,
    CONSTRAINT ORDER_ID PRIMARY KEY (ID),
    USER_ORDERED NUMBER NOT NULL,
    CONSTRAINT USER_FK FOREIGN KEY(USER_ORDERED) REFERENCES USERS(ID),
    ROOM NUMBER NOT NULL,
    CONSTRAINT ROOM_FK FOREIGN KEY(ROOM) REFERENCES ROOM(ID),
    DATE_FROM DATE,
    DATE_TO DATE,
    MONEY_PAID NUMBER(*, 2)
);

CREATE SEQUENCE HOTEL_SEQ MINVALUE 1 MAXVALUE 10000 START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE ROOM_SEQ MINVALUE 1 MAXVALUE 100000 START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE USER_SEQ MINVALUE 1 MAXVALUE 100000 START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE ORDER_SEQ MINVALUE 1 MAXVALUE 100000 START WITH 1 INCREMENT BY 1;